package day09_IfStatements;

public class PosNegZero {

    public static void main(String[] args) {
        System.out.println("--------------------------------------------------");


        int number = 4;
        if (number == 0) {
            System.out.println("Zero");
        } else if (number > 0) {
            System.out.println("Positive");
        } else {
            System.out.println("Negative");
        }


        System.out.println("--------------------------------------------------");


    }



}
